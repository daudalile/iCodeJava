public class forWhile {
    public static void main(String[] args) {
    //for loop
        System.out.println("Using for loop:");
      
        for (int i = 1; i <= 15; i++) {
            System.out.println("Iteration \n" + i);
        }
        
    //while loop
        System.out.println("Using while loop:");
      
        int j = 1;
      
        while (j <= 15) {
            System.out.println("Iteration " + j);
            j++;
        }
    }
}
